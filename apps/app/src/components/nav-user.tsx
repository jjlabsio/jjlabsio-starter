"use client";

import {
  IconBell,
  IconCreditCard,
  IconLogout,
  IconDeviceDesktop,
  IconMoon,
  IconDotsVertical,
  IconSun,
  IconSunMoon,
  IconUserCircle,
} from "@tabler/icons-react";
import { useTheme } from "next-themes";

import { Avatar, AvatarFallback } from "@repo/ui/components/avatar";
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuGroup,
  DropdownMenuItem,
  DropdownMenuSeparator,
  DropdownMenuSub,
  DropdownMenuSubContent,
  DropdownMenuSubTrigger,
  DropdownMenuTrigger,
} from "@repo/ui/components/dropdown-menu";
